# Crear una tupla
frutas = ("manzana", "platano", "naranja", "uva", "pera")

# Acceder a elementos de la tupla
print("La primera fruta es:", frutas[0])
print("La tercera fruta es:", frutas[2])

# Recorrer la tupla con un bucle
print("Lista de frutas:")
for fruta in frutas:
    print(fruta)